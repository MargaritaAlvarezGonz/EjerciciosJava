public class OperadoresDeComparacion {
    public static void main (String args[]){
        double sueldo1 = 10000.7878;
        double sueldo2 = 45264.56264;
        System.out.println(sueldo1>sueldo2);
        System.out.println(sueldo1<sueldo2);
        System.out.println(sueldo1>=sueldo2);
        System.out.println(sueldo1!=sueldo2);
        System.out.println(sueldo1==sueldo2);

    }
}
